function sendColors(shutdown = false){

	device.set_endpoint(3, 0, 1);

	const red =  new Array(144).fill(0);
	const green =  new Array(144).fill(0);
	const blue =  new Array(144).fill(0);
	const RGBData = new Array(144*3).fill(0);

	for(let iIdx = 0; iIdx < vKeys.length; iIdx++) {
		const iPxX = vKeyPositions[iIdx][0];
		const iPxY = vKeyPositions[iIdx][1];
		var col;

		if(shutdown){
			col = hexToRgb(shutdownColor);
		}else if (LightingMode === "Forced") {
			col = hexToRgb(forcedColor);
		}else{
			col = device.color(iPxX, iPxY);
		}
		const index = (Math.floor(vKeys[iIdx] / 12) * 36) + (vKeys[iIdx] % 12);
		//red[vKeys[iIdx]] = col[0];
		//green[vKeys[iIdx]] = col[1];
		//[vKeys[iIdx]] = col[2];
		RGBData[index + 0] = col[0];
		RGBData[index + 12] = col[1];
		RGBData[index + 24] = col[2];
	}
	let packet = [];

	packet[0x00]   = 0x00;
	packet[0x01]   = 0xA1;
	packet[0x02]   = 0x01;
	packet[0x03]   = 0x01;
	packet[0x04]   = 0xB4;
	packet = packet.concat(RGBData.splice(0, 60));
	device.write(packet, 65);

	for(let i = 0; i < 6;i++){
		packet = [];
		packet[0x00]   = 0x00;
		packet = packet.concat(RGBData.splice(0, 64));
		device.write(packet, 65);
	}


}

